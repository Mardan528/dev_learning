package com.cyebrtek.core;

public class Loops {

    public static void demoForLoop(){

        for (int i = 0; i < 5; i++) {

            System.out.println(i);;

        }
    }

    public static void demoEach(){

        String [] cars= {"Honda","Nissan","Frod"};

        for (String car : cars) {
            System.out.println(car);
        }


    }




}
